import sys

def main():
    input = sys.stdin.readline

    n, m = map(int, input().split())
    cards = list(map(int, input().split()))

    best = 0
    for i in range(n - 2):
        for j in range(i + 1, n - 1):
            for k in range(j + 1, n):
                s = cards[i] + cards[j] + cards[k]
                if s <= m and s > best:
                    best = s

    print(best)

if __name__ == "__main__":
    main()